/*
Question 91:
Create an array of your three favorite fruits and add a new fruit to the end of the array.

Explain & TIP: Adding elements to an array can be easily done using the .push() method. It allows
 you to add new items to the end of an array.
 */
// Defines an array with three favorite fruits
var favoriteFruits = ["Apple", "Banana", "Mango"];
favoriteFruits.push("Grapes"); // Adds "Mango" to the end of the array
console.log(favoriteFruits); // Outputs: ['Apple', 'Banana', 'Cherry', 'Mango']
// This line adds a new fruit to our list of favorites.
